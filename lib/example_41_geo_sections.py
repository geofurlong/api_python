# Example of converting from ELR and mileage ranges to positional co-ordinates.

from geofurlong import Geofurlong


gf = Geofurlong()

sections = (("Spalding", "WEB", 91, 403, 92, 1274), ("Thorniehill", "SBO", 13, 1100, 13, 1540))

for section in sections:
    location, elr, miles_from, yards_from, miles_to, yards_to = section

    # `gf.fmt` is an alias for `gf.format_mileage`
    print(
        f"Railway co-ordinates for {location} - ELR {elr} between {gf.fmt(miles_from, yards_from)} and {gf.fmt(miles_to, yards_to)}"
    )

    print("\nEasting / Northing co-ordinates")
    # `gf.ty` is an alias for `gf.build_total_yards`
    planar = gf.between(elr, gf.ty(miles_from, yards_from), gf.ty(miles_to, yards_to))
    for location in planar.coords:
        print(location)

    print("\nLongitude / Latitude co-ordinates")
    geographic = gf.between(elr, gf.ty(miles_from, yards_from), gf.ty(miles_to, yards_to), lon_lat=True)
    for location_lon_lat in geographic.coords:
        print(location_lon_lat)


