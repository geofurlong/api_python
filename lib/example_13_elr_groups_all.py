# Example of listing all ELR groupings.

from geofurlong import Geofurlong


gf = Geofurlong()

groups_set = set()
for elr_code in gf.elr_codes:
    elr_grouping = gf.elr(elr_code).grouping
    if len(elr_grouping) > 1:
        # Sets require elements to be hashable, so convert list to tuple.
        groups_set.add(tuple(elr_grouping))

# Convert set of tuples to list of lists.
groups_list = [tuple(group) for group in groups_set]
groups_list.sort()

for i, group in enumerate(groups_list, start=1):
    print(i, group)


